//常量引用允许等号两边类型不一致 → 类型转换
double val =3.14;
const int &ri = val; //发生类型转换，编译时产生一个整型的临时量


int i = 42;
const int &r1 = i;     //正确
const int &r2 = 4.12;   //正确
const int &r3 = r1 *2;   //正确：常量引用的初始值可以是任意表达式或初始值
// int &r4 = r1 *2;  //错误：非常量引用的类型必须一致，等号右边的类型是一个常量引用
// int &r5 = 4;  //错误：非常量引用的初始值类型必须为左值